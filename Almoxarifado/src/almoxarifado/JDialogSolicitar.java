    private void jButtonCancelaProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancelaProdutoActionPerformed

        if (jListaProdutosSolicitacao.getSelectedIndex() != -1) {

            solicitacao.cancelaProduto(solicitacao.getProdutos().get(jListaProdutosSolicitacao.getSelectedIndex()));
            refazListaSolicitacao();
            refazListaEstoque();
        } else {
        }
    }//GEN-LAST:event_jButtonCancelaProdutoActionPerformed

    private void jButtonSelecionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarProdutoActionPerformed

        if (JListaConsultarEstoque.getSelectedIndex() != -1) {

            JFormattedTextField quantidadeProduto = new JFormattedTextField();  //Novo jFormattedTextField para aplicação das máscaras.

            UtilMascaras.maskOnlyInt(quantidadeProduto);    //Aplica máscara para permitir apenas números inteiros

            JOptionPane.showMessageDialog(null, quantidadeProduto, "Insira a quantidade do Produto.", JOptionPane.PLAIN_MESSAGE);

            String quantidadeProdutoString = quantidadeProduto.getText().replace(" ", "");  //Apaga possíveis espaços que atrapalhariam na conversão.
            int quantidade = 0;

            verificaSeQuantidadeVazia(quantidadeProdutoString, quantidade);   //Verifica se a quantidade não é vazia para continuar.
        
        }
    }//GEN-LAST:event_jButtonSelecionarProdutoActionPerformed

    private void jButtonFinalizarSolicitacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFinalizarSolicitacaoActionPerformed

        if (solicitacao.getProdutos().size() > 0 && jComboSiglaDepartamento.getSelectedIndex() != -1 && jTextFieldDataPedido.getText() != null) {
            
            solicitacao.setDestino(GerenciadorDepartamentos.listaDepartamentos.get(jComboSiglaDepartamento.getSelectedIndex()));
            
            if(solicitacao.setDataPedido(jTextFieldDataPedido.getText()) == true){
            
                GerenciadorSolicitacoes.adicionaSolicitacao(solicitacao);
                solicitacao.finalizaSolicitacao();
                JOptionPane.showMessageDialog(null, "Solicitação efetuada com Sucesso!\nCod: "+solicitacao.getSolicitacaoID());
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Informe uma data válida!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Informe todos os campos da Solicitação!");
        }
    }//GEN-LAST:event_jButtonFinalizarSolicitacaoActionPerformed

    private void jButtonCancelarSolicitacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancelarSolicitacaoActionPerformed
        solicitacao.getProdutos().forEach((produto) -> {
            int reposicao = solicitacao.getProdutos().get(solicitacao.getProdutos().indexOf(produto)).getQuantidadeSolicitacao()
                    + GerenciadorEstoque.estoque.get(GerenciadorEstoque.estoque.indexOf(produto)).getQuantidade();

            GerenciadorEstoque.repoeProduto(produto, reposicao);
        });

        dispose();
    }//GEN-LAST:event_jButtonCancelarSolicitacaoActionPerformed

