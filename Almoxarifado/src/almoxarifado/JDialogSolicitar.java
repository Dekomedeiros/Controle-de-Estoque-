        private void verificaSeQuantidadeVazia(String quantidadeProdutoString, int quantidade) {
            if (quantidadeProdutoString.equals("")) { 

            } else {
                quantidade = Integer.parseInt(quantidadeProdutoString);
                if (quantidade > 0) {
                    solicitacao.adicionaProduto(JListaConsultarEstoque.getSelectedIndex(), quantidade);
                    refazListaSolicitacao();
                    refazListaEstoque();
                } else {
                    JOptionPane.showMessageDialog(null, "Insira ao menos 1 produto à sua Solicitação");
                }

            }
    }


